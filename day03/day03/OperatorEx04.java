package day03;

public class OperatorEx04 {
	public static void main(String[] args) {
		
		//대입 연산자
		int a=6, b=5;
		
		a+=3; // > a = a+3
		b=+3; // > b = 3
		
		System.out.println(a);
		System.out.println(b);
		
		a -= 4; // 2
		a *= 10; // 20
		a /= 5; //4
		a %= 2; //0
		
		System.out.println(a);
	}
}
